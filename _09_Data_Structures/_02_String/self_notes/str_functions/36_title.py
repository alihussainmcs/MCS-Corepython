"""
title()
Returns "titlecased" version of string, that is, all words begin with uppercase and the rest are lowercase.

"""

txt = "Welcome to my world"

x = txt.title()

print(x)

print()

txt = "Welcome to my 2nd world"

x = txt.title()

print(x)

print()

txt = "hello b2b2b2 and 3g3g3g"

x = txt.title()

print(x)

print()

