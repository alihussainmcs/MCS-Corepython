print("...............For loop with dictionary .........................")

dict_1 = {101: 'id', 102: 'name', 103: True, 104: '100k'}

print("Dictionary1 is  ", dict_1)

print()

print("Element in dict_1 are : ")

for i in dict_1:
    print(i)

print()


print("Dictionary keys are : ")

for i in dict_1.keys():
    print(i)

print()

print("Dictionary values are : ")

for i in dict_1.values():
    print(i)

print()

print("Dictionary items are : ")

for i in dict_1.items():
    print(i)
